    public function create(Request $request)
    {
       
        $id = $request['id'];
        $user_status = User::find($id);
        if ($user_status->status == "1") {
            $user_status->status = "0";
        } else {
            $user_status->status = "1";
        }
        $user_status->save();
        return response()->json(['data' => $user_status]);
     
     
 
  
